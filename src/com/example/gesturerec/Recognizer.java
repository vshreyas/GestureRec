	public String match(ArrayList<Point2> stroke, Vector<ArrayList<Point2>> templates) {
		int output = -1;
		double b = Double.POSITIVE_INFINITY,d;
		double score;
		for(int i = 0;i < templates.size();i++) {
			ArrayList<Point2> T = templates.get(i);
			d = distAtBestAngle(stroke, T, -th, th, thd);
			if(d < b) {
				b = d;
				output = i;
			}
		}
		score = 1 - (b / 0.5*Math.sqrt(2*size*size));
		System.out.println(score);
		if((output == 40 || output == 12 ) && score < 0.2) output = distinguish(stroke, output); 


		return lookup.get(output);
	}

	private int distinguish(ArrayList<Point2> stroke, int output) {
		if(output == 40 || output == 12 ) {
			int n= stroke.size();
			double f1 = Math.atan2((stroke.get(n).y - stroke.get(n-1).y),(stroke.get(n).x - stroke.get(n).x));
			if(f1 < 0) return 40;
			else return 12;
		}
		return 0;
	}

	private double distAtBestAngle(ArrayList<Point2> stroke, ArrayList<Point2> T, double th1, double th2,
			double thd) {
		double x1 = phi * th1 + (1-phi)*th2;
		double x2 = phi * th2 + (1-phi)*th1;
		double f1 = distAtAngle(T, x1, stroke);
		double f2 = distAtAngle(T, x2, stroke);
		while(Math.abs(th2 - th1) > thd) {
			if(f1 < f2) {
				th2 = x2;
				x2 = x1;
				f2 = f1;
				x1 = phi*th1 + (1 - phi) * th2;
				f1 = distAtAngle(T, x1, stroke);
			}
			else {
				th1 = x1;
				x1 = x2;
				f1 = f2;
				x2 = (1 -phi) * th1 + phi*th2;
				f2 = distAtAngle(T, x2, stroke);
			}
			return Math.min(f1, f2);

		}
		return 0;
	}

	private double distAtAngle(ArrayList<Point2> t, double theta, ArrayList<Point2> stroke) {
		ArrayList<Point2> newpts = rotateBy(stroke, getCentroid(stroke), theta);
		double d = pathDistance(newpts, t);
		return d;
	}

	private double pathDistance(ArrayList<Point2> newpts, ArrayList<Point2> t) {
		double d = 0;
		if(newpts.size() <= 35 || t.size() <= 35)
		{
			Log.v("Dang", "it");
		}
		for(int i= 0; i < numSamples - 1;i++) {
			d = d + Point2.Distance(newpts.get(i), t.get(i));
		}
		return d / numSamples;

	}

	public ArrayList<Point2> preprocess(ArrayList<Point2> stroke) {
		ArrayList<Point2> t = txToOrigin(scaleToSquare(rotateToZero(resample(stroke))));
		return t;
	}

	public ArrayList<Point2> resample(ArrayList<Point2> s) {
		ArrayList<Point2> stroke = new ArrayList<Point2>(s);
		ArrayList<Point2> sample = new ArrayList<Point2>();
		sample.add(stroke.get(0));
		double I = pathLength(stroke)/(numSamples - 1);
		double D = 0;
		double d;
		for(int i = 0;i < stroke.size() - 1;i++) {
			d = Point2.Distance(stroke.get(i), stroke.get(i+1));
			if(D+d >= I) {
				double qx = stroke.get(i).x + ((I - D) / d) * (stroke.get(i+1).x - stroke.get(i).x);
				double qy = stroke.get(i).y + ((I - D) / d) * (stroke.get(i+1).y - stroke.get(i).y);
				Point2 q = new Point2(qx, qy);
				sample.add(q);
				stroke.add(i+1, q);
				D = 0;
			}
			else D = d+D;
		}
		return sample;

	}

	public ArrayList<Point2> rotateToZero(ArrayList<Point2> stroke) {
		ArrayList<Point2> rot;
		Point2 c = getCentroid(stroke);
		double theta = Math.atan2(c.y - stroke.get(0).y, c.x - stroke.get(0).x);
		rot = rotateBy(stroke, c, -theta);
		return rot;


	}


	private ArrayList<Point2> rotateBy(ArrayList<Point2> s, Point2 c, double theta) {
		ArrayList<Point2> rot = new ArrayList<Point2>();
		for(int i = 0;i < s.size();i++) {
			double x = (s.get(i).x - c.x) * Math.cos(theta) - (s.get(i).y - c.y) * Math.sin(theta) + c.x;
			double y = (s.get(i).x - c.x) * Math.sin(theta) + (s.get(i).y - c.y) * Math.cos(theta) + c.y;
			rot.add(new Point2(x,y));
		}
		return rot;
	}

	private Point2 getCentroid(ArrayList<Point2> stroke) {
		double x = 0;
		double y = 0;
		for(int i = 0;i < stroke.size();i++) {
			x += (stroke.get(i).x)/stroke.size();
			y += (stroke.get(i).y)/stroke.size();
		}
		Point2 p = new Point2(x,y);
		return p;
	}

	private double pathLength(ArrayList<Point2> stroke) {
		double pathdist = 0;
		for(int i=0;i< stroke.size() - 1;i++) {
			pathdist += Point2.Distance(stroke.get(i), stroke.get(i+1));
		}
		return pathdist;
	}

	public ArrayList<Point2> scaleToSquare(ArrayList<Point2> stroke) {
		double minx,maxx,miny,maxy;
		minx = miny = Double.POSITIVE_INFINITY;
		maxx = maxy = Double.NEGATIVE_INFINITY;
		for(int i=0;i < stroke.size();i++) {
			if(stroke.get(i).x > maxx)maxx = stroke.get(i).x;
			if(stroke.get(i).y > maxy)maxy = stroke.get(i).y;
			if(stroke.get(i).x < minx)minx = stroke.get(i).x;
			if(stroke.get(i).y < miny)miny = stroke.get(i).y;
		}
		ArrayList<Point2> scaled = new ArrayList<Point2>();
		double qx,qy;
		for(int i=0;i < stroke.size();i++) {
			if(maxx == minx)qx = stroke.get(i).x;
			if(maxy == miny)qy = stroke.get(i).y;
			qx = stroke.get(i).x * size / (maxx -minx);
			qy = stroke.get(i).y * size / (maxy - miny);
			scaled.add(new Point2(qx,qy));
		}
		return scaled;
	}

	public ArrayList<Point2> txToOrigin(ArrayList<Point2> stroke) {
		ArrayList<Point2> translated = new ArrayList<Point2>();
		Point2 c = getCentroid(stroke);
		for(int i = 0;i < stroke.size();i++) {
			double qx = stroke.get(i).x - c.x;
			double qy = stroke.get(i).y - c.y;
			Point2 q = new Point2(qx,qy);
			translated.add(q);
		}
		return translated;
	}


